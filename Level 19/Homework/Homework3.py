# თქვენი სახელი
my_name = "Alexander"

while True:
    # მომხმარებლისგან სახელის შემოტანა
    user_name = input("გთხოვთ შეიყვანოთ სახელი: ")
    
    # შეამოწმოთ თუ მომხმარებლის სახელი უდრის თქვენს სახელს
    if user_name == my_name:
        print("სახელები ემთხვევა!")
        break
    else:
        print("სახელები არ ემთხვევა, სცადეთ ისევ.")